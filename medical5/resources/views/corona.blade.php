                            <p>What is pneumonia or pneumonia?
✔ pneumonia is an inflammation of the lung vesicles that causes them to be filled with pus and fluid. The incidence varies between mild and severe life-threatening, especially among children, the elderly, people with chronic diseases and low immunity.
✔ Reason: infection, either viral or bacterial and to a lesser degree microbial and some medications.
✔ risk factors: smoking, chronic lung disease, heart disease, diabetes, a viral infection that affects the respiratory system such as influenza
Signs and symptoms: cough (dry or sputum), fever, difficulty breathing, rapid heartbeat, loss of appetite, feeling tired, chest pain, sweating and tremor.
✔ Treatment:
Pharmacotherapy in the form of antimicrobial drugs, the type of which varies according to the cause of pneumonia
- In severe cases, the patient is transferred to the hospital, and there are given intravenous antidotes in addition to oxygen therapy, either by the facial mask or by the nasal tube to maintain the level of blood oxygenation within the normal limits.
