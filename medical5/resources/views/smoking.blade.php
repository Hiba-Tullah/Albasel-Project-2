                            <a href="/smoking" class="headline">Smoking and heart health</a>
                            <p>Smoking causes structural changes in the heart and the blood it presses into the body, which weakens its ability to function normally. As a result, the risk of developing cardiovascular disease, including coronary heart disease and stroke, increases.
Smoking has many negative effects on the health of the body and heart, including:
Arterial walls damage by facilitating plaque buildup, which leads to clogged arteries.
It lowers blood oxygen levels, forcing the heart to make a double effort to supply the body with the oxygen it needs.
Reduces blood flow to the body's organs, resulting in blood vessel contact or thickening.
Increases blood pressure and heart rate.
It increases blood viscosity and cholesterol level, which increases the possibility of strokes that may lead to strokes or heart attacks.. </p>
