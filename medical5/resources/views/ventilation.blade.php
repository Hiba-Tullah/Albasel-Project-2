The device is defined as an automated, supportive method of ventilation that helps the patient secure his respiratory need when self-ventilation fails. The goal is to avoid hypoxia and avoid its effects on the organs of the body while the patient is able to carry out the respiratory burden on his own. The competitor patterns have ranged from negative pressure to the most commonly used positive pressure in which the ventilator enters the gases of inhalation under positive pressure and the device connects to the patient via a synthetic airway, the tracheal tube or the tracheal cannula, which secures the air from the ventricle to the trachea. Endotracheal intubation is done either by a nasal tracheal tube entering from the nose to the trachea or by an endotracheal oral tube entering from the mouth. The laryngoscopy is inserted into the tube via the vocal cords to the trachea, while the endotracheal biopsy is carried out between the annular cartilages 2, 3, 3 and 4 to insert the tracheal cannula.

Mechanism: ventilator_ equipment_machine:
1- The respirator
2- Intubation equipment including: laryngoscope / endotracheal tube / catheter-like detachment / oxygen source with AMBO / duct tape to attach the tube with a 10mm balloon syringe / Magill tongs / small diameter metal guide inserted into the tube's lumen
3- A device for monitoring heart changes and oxygenation

#Standard_indices
- Respiratory speed more than 35 breaths per minute
Partial blood oxygen pressure less than 60 mmHg
The partial pressure of carbon dioxide in the blood is greater than 50 mm Hg

# Cases_representative_mechanical_ventilation
First - Respiratory failure caused by:
* Mechanical failure, neuromuscular diseases, Guillain-Barre syndrome and severe muscle weakness
* Structural nerve disorders such as trauma or chest injuries
Infectious diseases such as pneumonia and tuberculosis

Second- Gas exchange disorders in the lungs:
* COPD diseases such as asthma, chronic bronchitis, emphysema
Lung edema, pulmonary decline or pulmonary fibrosis

III- Patients of general anesthesia, in addition to patients with heart attacks, need respiratory support in order to be awakened from general anesthesia or bypassing the condition of cardiac disturbance

