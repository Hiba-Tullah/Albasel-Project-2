4. The occurrence of self-PEEP in those with airway obstruction.
5. Oxygen poisoning when it is used 100% and appears after 9 hours
6. Alkaline acid balance.
7. Severe renal insufficiency due to lack of cardiac outcomes and decreased renal perfusion
8. The wind wind.
